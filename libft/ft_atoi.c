#include "libft.h"

int		ft_atoi(const char *str)
{
	int		res;
	int		negative;
	size_t	i;

	i = 0;
	res = 0;
	negative = 0;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\v' ||
			str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		negative = 101;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res *= 10;
		res += str[i] - '0';
		i++;
	}
	if (negative == 101)
		res = -res;
	return (res);
}
