#include "libft.h"

void	ft_putnbr(int n)
{
	int		mini;

	mini = 0;
	if (n > 2147483647 || n < -2147483648)
		return ;
	if (n == -2147483648)
	{
		mini = 101;
		ft_putnbr(n / 10);
	}
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	else
		ft_putchar(n + '0');
	if (mini == 101)
		ft_putchar('8');
}
