#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*d;
	char	*s;

	d = (char*)dst;
	s = (char*)src;
	if (s < d)
	{
		s += len - 1;
		d += len - 1;
		while (len > 0)
		{
			*d-- = *s--;
			len--;
		}
	}
	else
		ft_memcpy(d, s, len);
	return (dst);
}
