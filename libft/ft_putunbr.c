#include "libft.h"

void	ft_putunbr(unsigned int n)
{
	int		mini;

	mini = 0;
	if (n > 4294967295)
		return ;
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	else
		ft_putchar(n + '0');
}
